const byName = new Map();
for (const it of ING) {
  byName.set(it.name, it);
  for (const a of it.aliases) byName.set(a, it);
}

// 자유 텍스트(영수증 상품명 등) → 마스터 재료 매핑
export function findIng(raw) {
  if (!raw) return null;
  const t = String(raw).replace(/\s/g, '');
  if (byName.has(t)) return byName.get(t);
  for (const [key, val] of byName) {
    if (t.includes(key) || key.includes(t)) return val;
  }
  return null;
}

export function defaultShelf(ing, location) {
  if (!ing) return 7;
  return ing.shelf[location] ?? ing.shelf.fridge ?? ing.shelf.room ?? ing.shelf.freezer ?? 7;
}

export function defaultLocation(ing) {
  if (!ing) return 'fridge';
  if (ing.shelf.fridge) return 'fridge';
  if (ing.shelf.room) return 'room';
  return 'freezer';
}
